def flatten_items(data):
    """Walk the whole nested structure and pull every item out into one flat list,
    tagging each with its difficulty and content_type along the way."""
    flat = []
    for difficulty, content_types in data.items():
        for content_type, items in content_types.items():
            for item in items:
                tagged_item = {
                    **item,
                    "difficulty": difficulty,
                    "content_type": content_type,
                }
                flat.append(tagged_item)
    return flat


