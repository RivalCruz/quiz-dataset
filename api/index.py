import os
import random
from typing import Literal

from fastapi import FastAPI, Header, HTTPException, Query
from fastapi.middleware.cors import CORSMiddleware
from pydantic import BaseModel

from data import all_items, items_by_id

Difficulty = Literal["easy", "medium", "hard"]
ContentType = Literal["text", "image", "video"]
Guess = Literal["AI", "Human"]


class AnswerRequest(BaseModel):
    item_id: str
    guess: Guess


app = FastAPI(
    title="AI vs Human Quiz API",
    description="FastAPI dataset service for the UNESCO Youth Hackathon game.",
    version="2.1.0",
)


def get_allowed_origins() -> list[str]:
    raw_value = os.getenv("ALLOWED_ORIGINS", "*")
    origins = [origin.strip() for origin in raw_value.split(",") if origin.strip()]
    return origins or ["*"]


app.add_middleware(
    CORSMiddleware,
    allow_origins=get_allowed_origins(),
    allow_credentials=False,
    allow_methods=["GET", "POST", "OPTIONS"],
    allow_headers=["*"],
)


def build_media_url(content: str) -> str:
    """Convert a relative dataset path into a public Vercel Blob URL."""
    if content.startswith(("http://", "https://")):
        return content

    blob_base_url = os.getenv("BLOB_BASE_URL", "").strip().rstrip("/")

    if not blob_base_url:
        raise HTTPException(
            status_code=500,
            detail="BLOB_BASE_URL is not configured in Vercel.",
        )

    return f"{blob_base_url}/{content.lstrip('/')}"


def determine_answer(item: dict) -> Guess:
    """Normalize the inconsistent source descriptions into AI or Human."""
    source = str(item.get("source", "")).lower().strip()

    ai_markers = (
        "generated by ai",
        "ai-generated",
        "classified as ai",
        "ai work",
    )
    human_markers = (
        "written by human",
        "real human",
        "human-produced",
        "taken by human",
        "real news",
        "this is real",
        "real -",
    )

    if any(marker in source for marker in ai_markers):
        return "AI"
    if any(marker in source for marker in human_markers):
        return "Human"

    raise HTTPException(
        status_code=500,
        detail=f"Dataset item {item.get('id')} has an unrecognized source value.",
    )


def serialize_public_item(item: dict) -> dict:
    """Return game content without exposing the hidden answer."""
    public_item = {
        "id": item["id"],
        "topic": item.get("topic", ""),
        "content": item.get("content"),
        "difficulty": item["difficulty"],
        "content_type": item["content_type"],
    }

    if item["content_type"] in {"image", "video"}:
        public_item["content"] = build_media_url(str(item["content"]))

    return public_item


def serialize_full_item(item: dict) -> dict:
    full_item = dict(item)
    full_item["answer"] = determine_answer(item)

    if item["content_type"] in {"image", "video"}:
        full_item["content"] = build_media_url(str(item["content"]))

    return full_item


@app.get("/")
def root() -> dict:
    return {
        "name": "AI vs Human Quiz API",
        "status": "online",
        "total_items": len(all_items),
        "documentation": "/docs",
        "quiz_example": "/api/quiz?difficulty=easy&count=6",
    }


@app.get("/api/health")
def health() -> dict:
    return {"status": "ok", "total_items": len(all_items)}


@app.get("/api/items")
def get_items(difficulty: Difficulty | None = None,content_type: ContentType | None = None,) -> dict:
    selected_items = all_items

    if difficulty is not None:
        selected_items = [
            item for item in selected_items if item["difficulty"] == difficulty
        ]

    if content_type is not None:
        selected_items = [
            item for item in selected_items if item["content_type"] == content_type
        ]

    return {
        "count": len(selected_items),
        "items": [serialize_public_item(item) for item in selected_items],
    }


@app.get("/api/items/{item_id}")
def get_item(item_id: str) -> dict:
    item = items_by_id.get(item_id)
    if item is None:
        raise HTTPException(status_code=404, detail="Item not found")

    return serialize_public_item(item)


@app.get("/api/quiz")
def create_quiz(difficulty: Difficulty, count: int = Query(default=6, ge=1, le=16),) -> dict:
    pool = [item for item in all_items if item["difficulty"] == difficulty]
    selected_items = random.sample(pool, min(count, len(pool)))

    return {
        "difficulty": difficulty,
        "count": len(selected_items),
        "items": [serialize_public_item(item) for item in selected_items],
    }


@app.post("/api/answer")
def check_answer(answer_request: AnswerRequest) -> dict:
    item = items_by_id.get(answer_request.item_id)
    if item is None:
        raise HTTPException(status_code=404, detail="Item not found")

    correct_answer = determine_answer(item)

    return {
        "id": item["id"],
        "guess": answer_request.guess,
        "correct": answer_request.guess == correct_answer,
        "answer": correct_answer,
        "source": item.get("source", ""),
        "explanation": item.get("explanation", ""),
        "key_points": item.get("key_points", []),
        "content": (
            build_media_url(str(item["content"]))
            if item["content_type"] in {"image", "video"}
            else item.get("content")
        ),
    }


@app.get("/api/admin/items")
def get_full_dataset(x_admin_key: str | None = Header(default=None, alias="X-Admin-Key"),) -> dict:
    expected_key = os.getenv("ADMIN_KEY", "").strip()

    if not expected_key:
        raise HTTPException(status_code=503, detail="ADMIN_KEY is not configured")
    if x_admin_key != expected_key: 
        raise HTTPException(status_code=401, detail="Invalid admin key")
    
    return {
        "count": len(all_items),
        "items": [serialize_full_item(item) for item in all_items],
    }